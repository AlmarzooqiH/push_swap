#include "push_swap.h"

int	pa(t_stack **a, t_stack **b, int from)
{
	if (!(*a) || !(*b) || is_empty(b))
		return (0);
	if ((*a)->t >= 0 && (*a)->t <= (*a)->sz)
		(*a)->s[(*a)->t - 1] = (*b)->s[(*b)->t];
	if ((*a)->t - 1 >= 0)
		(*a)->t--;
	if ((*b)->t + 1 <= (*b)->sz)
	{
		(*b)->s[(*b)->t] = 0;
		(*b)->t++;
	}
	if (from == 0)
		ft_printf("pa\n");
	else if (from == 1)
		ft_printf("pb\n");
	return (1);
}

int	pb(t_stack **a, t_stack **b, int from)
{
	return (pa(b, a, from));
}
